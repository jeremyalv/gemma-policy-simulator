/**
 * QuoteCard — single representative persona quote card.
 * Shows: persona info, approval (1–5), emotion badge, rationale.
 *
 * CONTRACT: approval field is integer 1–5. Display as score, not %.
 */

import { useState } from 'react'
import { Box, Text, Group, Badge, Stack, Modal, Button, CopyButton, Tooltip, ActionIcon } from '@mantine/core'
import { Quote, Copy, Check } from 'lucide-react'
import { approvalColor, emotionColor } from '@/lib/format'
import type { RepresentativeQuote } from '@/api'

const APPROVAL_LABELS: Record<number, string> = {
  1: 'Strongly Oppose',
  2: 'Oppose',
  3: 'Neutral',
  4: 'Support',
  5: 'Strongly Support',
}

interface QuoteCardProps {
  quote: RepresentativeQuote
}

// ── Citation formatter (UC-14) ────────────────────────────────────────────────

function buildCitation(quote: RepresentativeQuote): string {
  const today = new Date().toLocaleDateString('en-US', {
    year: 'numeric', month: 'long', day: 'numeric',
  })
  return (
    `${quote.name} (age ${quote.age}, ${quote.occupation}, ${quote.city}, ${quote.state}). ` +
    `Approval: ${quote.approval}/5. Dominant emotion: ${quote.emotion}. ` +
    `"${quote.rationale}" ` +
    `[Synthetic persona generated by InfiniPol. Retrieved ${today}.]`
  )
}

// ── Component ─────────────────────────────────────────────────────────────────

export function QuoteCard({ quote }: QuoteCardProps) {
  const { name, age, occupation, city, state, approval, emotion, rationale } = quote
  const aColor = approvalColor(approval)
  const eColor = emotionColor(emotion)
  const [citationOpen, setCitationOpen] = useState(false)
  const citation = buildCitation(quote)

  return (
    <>
    {/* ── Citation modal ──────────────────────────────────────────── */}
    <Modal
      opened={citationOpen}
      onClose={() => setCitationOpen(false)}
      title={
        <Group gap={8}>
          <Quote size={16} color="var(--color-accent-primary)" />
          <Text fw={600} size="sm" c="var(--color-text-primary)">Cite this persona</Text>
        </Group>
      }
      size="md"
      styles={{
        header: { borderBottom: '1px solid var(--color-border-subtle)', padding: '14px 20px' },
        body:   { padding: '20px' },
      }}
    >
      <Stack gap="md">
        <Box
          style={{
            backgroundColor: 'var(--color-bg-subtle)',
            borderRadius: 8,
            padding: '14px 16px',
            border: '1px solid var(--color-border-default)',
          }}
        >
          <Text size="sm" c="var(--color-text-secondary)" lh={1.7} style={{ fontFamily: 'IBM Plex Mono, monospace', fontSize: 12 }}>
            {citation}
          </Text>
        </Box>
        <Text size="xs" c="var(--color-text-tertiary)" lh={1.5}>
          This persona is synthetic — generated by AI based on demographic parameters.
          Not a real individual. Do not attribute real opinions to this data.
        </Text>
        <Group justify="flex-end">
          <CopyButton value={citation} timeout={2000}>
            {({ copied, copy }) => (
              <Button
                size="xs"
                leftSection={copied ? <Check size={12} /> : <Copy size={12} />}
                color={copied ? 'teal' : 'blue'}
                variant={copied ? 'filled' : 'outline'}
                onClick={copy}
              >
                {copied ? 'Copied!' : 'Copy citation'}
              </Button>
            )}
          </CopyButton>
        </Group>
      </Stack>
    </Modal>

    {/* ── Card ────────────────────────────────────────────────────── */}
    <Box
      style={{
        border: '1px solid var(--color-border-subtle)',
        borderLeft: `4px solid ${aColor}`,
        borderRadius: 10,
        backgroundColor: 'var(--color-bg-surface)',
        padding: '18px 20px',
        display: 'flex',
        flexDirection: 'column',
        gap: 14,
        breakInside: 'avoid',
      }}
    >
      {/* Rationale — top, most prominent */}
      <Text
        size="md"
        lh={1.8}
        c="var(--color-text-primary)"
        style={{
          fontFamily: 'Source Serif 4, serif',
          fontStyle: 'italic',
        }}
      >
        "{rationale}"
      </Text>

      {/* Persona info */}
      <Stack gap={8}>
        <Group justify="space-between" align="flex-start" wrap="nowrap">
          {/* Avatar + name chip */}
          <Group gap={8} wrap="nowrap">
            <Box
              style={{
                width: 28,
                height: 28,
                borderRadius: '50%',
                backgroundColor: `${aColor}20`,
                border: `1px solid ${aColor}50`,
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                flexShrink: 0,
              }}
            >
              <Text size="xs" fw={700} style={{ color: aColor, lineHeight: 1 }}>
                {name.charAt(0).toUpperCase()}
              </Text>
            </Box>
            <Stack gap={2}>
              <Text size="sm" fw={600} c="var(--color-text-primary)">{name}</Text>
              <Text size="xs" c="var(--color-text-tertiary)">Age {age} · {occupation} · {city}, {state}</Text>
            </Stack>
          </Group>

          {/* Approval badge */}
          <Box
            style={{
              backgroundColor: `${aColor}15`,
              border: `1px solid ${aColor}40`,
              borderRadius: 6,
              padding: '4px 10px',
              textAlign: 'center',
              flexShrink: 0,
            }}
          >
            <Text
              size="xs"
              fw={700}
              style={{
                color: aColor,
                fontFamily: 'Source Serif 4, serif',
                fontSize: '1.1rem',
              }}
            >
              {approval} / 5
            </Text>
            <Text size="10px" style={{ color: aColor, opacity: 0.8 }}>
              {APPROVAL_LABELS[approval] ?? ''}
            </Text>
          </Box>
        </Group>

        {/* Emotion + cite row */}
        <Group justify="space-between" align="center">
          <Badge
            size="xs"
            variant="light"
            style={{
              backgroundColor: `${eColor}15`,
              color: eColor,
              border: `1px solid ${eColor}30`,
              textTransform: 'capitalize',
              fontWeight: 500,
            }}
          >
            {emotion}
          </Badge>
          <Tooltip label="Cite this persona" position="top" withArrow>
            <ActionIcon
              size="xs"
              variant="subtle"
              color="gray"
              aria-label="Cite this persona"
              onClick={() => setCitationOpen(true)}
            >
              <Quote size={12} />
            </ActionIcon>
          </Tooltip>
        </Group>
      </Stack>
    </Box>
    </>
  )
}
